#include <stdio.h>

int ft_tolower(int c)
{
	if(c >= 65 && c <= 90)
	{
		c = c + 32;
		return c;
	}
	else
		return c;
}

int main()
{
	printf("%c", ft_tolower('b'));
	return 0;
}

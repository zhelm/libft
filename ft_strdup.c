#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	a;

	a = 0;
	str = (char *)malloc(sizeof(char) * ft_strlen(s1) + 1);
	if (str == NULL)
		return (NULL);
	while (s1[a])
	{
		str[a] = s1[a];
		a++;
	}
	str[a] = '\0';
	return (str);
}
